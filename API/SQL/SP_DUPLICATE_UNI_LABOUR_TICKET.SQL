CREATE PROCEDURE DUPLICATE_RECORD_UNI_LABOR_TICKET
(
    @TRANS_ID INT
)
AS
BEGIN
    INSERT INTO UNI_LABOR_TICKET (WORKORDER_TYPE, WORKORDER_BASE_ID, WORKORDER_LOT_ID, WORKORDER_SPLIT_ID, WORKORDER_SUB_ID, OPERATION_SEQ_NO, TYPE, TYPE_STRING, EMPLOYEE_ID, RESOURCE_ID, 
		CLOCK_IN, CLOCK_OUT, HOURS_WORKED, INDIRECT_CODE, INDIRECT_ID, WORK_LOCATION, WORK_TIME)
	SELECT WORKORDER_TYPE, WORKORDER_BASE_ID, WORKORDER_LOT_ID, WORKORDER_SPLIT_ID, WORKORDER_SUB_ID, OPERATION_SEQ_NO, TYPE, TYPE_STRING, EMPLOYEE_ID, RESOURCE_ID, 
		CLOCK_IN, CLOCK_OUT, HOURS_WORKED, INDIRECT_CODE, INDIRECT_ID, WORK_LOCATION, WORK_TIME
		FROM UNI_LABOR_TICKET WHERE TRANSACTION_ID = @TRANS_ID 
    --SELECT SCOPE_IDENTITY() AS DuplicatedPrimaryKey
END



GRANT EXECUTE ON DUPLICATE_RECORD_UNI_LABOR_TICKET to PUBLIC